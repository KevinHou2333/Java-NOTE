package mathDemo;

public class Demo01 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5));//绝对值
        System.out.println(Math.ceil(3.3));//大于该数字的最小整数
        System.out.println(Math.floor(3.3));//小于该数字的最小整数
       //Math.round()是四舍五入
        System.out.println(Math.round(5.5));//6
        System.out.println(Math.round(5.4));//5

    }
}
